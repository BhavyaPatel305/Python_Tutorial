#List slicing

studnts = ["John", "Jennie", "Jim", "Jack", "Joe"]

# digits=[12,22,34,56,7,9]

print(studnts)
print(studnts[0:3])
print(studnts[1:4])
print(studnts[:3])
print(studnts[2:])
print(studnts[-1])
print(studnts[0:-1])
#reverse the list using slicing
print(studnts[::-1])
print(studnts[::-3])
print(studnts[-1::-1])
